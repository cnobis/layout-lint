#!/usr/bin/env node
// Generates dist/core/wasm-data.js with base64-encoded payloads for the
// grammar WASM (layout_lint.wasm at repo root) and the web-tree-sitter
// runtime WASM (node_modules/web-tree-sitter/tree-sitter.wasm).
//
// Run after `tsc`. Overwrites the placeholder file emitted from
// src/core/wasm-data.ts.

import { readFile, writeFile, access } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

const grammarPath = resolve(root, "layout_lint.wasm");
const runtimePath = resolve(root, "node_modules/web-tree-sitter/tree-sitter.wasm");
const outPath = resolve(root, "dist/core/wasm-data.js");

async function exists(path) {
  try {
    await access(path);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  for (const [label, path] of [
    ["grammar", grammarPath],
    ["runtime", runtimePath],
  ]) {
    if (!(await exists(path))) {
      console.error(`build-wasm-inline: ${label} WASM not found at ${path}`);
      process.exit(1);
    }
  }
  if (!(await exists(resolve(root, "dist/core")))) {
    console.error("build-wasm-inline: dist/core not found. Run `tsc` first.");
    process.exit(1);
  }

  const [grammar, runtime] = await Promise.all([
    readFile(grammarPath),
    readFile(runtimePath),
  ]);

  const grammarB64 = grammar.toString("base64");
  const runtimeB64 = runtime.toString("base64");

  const body = `// Generated by scripts/build-wasm-inline.mjs — do not edit.
export const GRAMMAR_WASM_BASE64 = ${JSON.stringify(grammarB64)};
export const RUNTIME_WASM_BASE64 = ${JSON.stringify(runtimeB64)};
`;

  await writeFile(outPath, body, "utf8");
  const kib = (n) => `${(n / 1024).toFixed(1)} KiB`;
  console.log(
    `build-wasm-inline: wrote ${outPath} (grammar ${kib(grammar.length)}, runtime ${kib(runtime.length)})`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
